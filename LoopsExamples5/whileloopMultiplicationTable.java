package LoopsExamples5;

public class whileloopMultiplicationTable {
    public static void main(String[] args){
        // int nub = 2;
        // int nub = 8;
        // int nub = 4;
        int nub = 5;
        int i = 1;
        while(i <= 10){
            System.out.println(nub +" X " + i + " = " + (nub*i));
            i++; // incrementing i by 1 each time
        }
    }
}
