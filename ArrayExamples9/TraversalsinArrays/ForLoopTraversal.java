package ArrayExamples9.TraversalsinArrays;

public class ForLoopTraversal {
    public static void main(String[] args){
        int[] arr = {8,14,15};
        System.out.println("For loop iterations : ");

        for(int i = 0 ; i < arr.length; i++){
            System.out.println("Number of index " + i + " is " + arr[i]);
        }
    }
    
}
